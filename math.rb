#There are built in Ruby Math Objects and constants. (The :: notation is the scope operator).
puts(Math::PI) #Value of PI
puts(Math::E) #Value of e (base of natural logarithms)
puts(Math.cos(Math::PI/3)) #.cos computes the cosine 
puts(Math.tan(Math::PI/4)) #.tan returns the tangent 
puts(Math.log(Math::E**2)) #.log returns the natural logarithm 
puts((1 + Math.sqrt(5))/2) #.sqrt returns the non-negative square root